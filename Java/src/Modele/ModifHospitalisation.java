    private void recuperer(String test){
       /* System.out.println("A");
        List_des_employes inf = new List_des_employes();
        System.out.println("B");
        inf.deplacement();
        System.out.println("C");*/
        try{
            
            String rec = test;
            String requete = "select * from HOSPITALISATION where no_malade ='"+rec+"'";
            
            ps =conn.prepareStatement(requete);
            rs = ps.executeQuery();
            if(rs.next()){
                String t1 = rs.getString("no_malade");
                txt_num.setText(t1);
                String t2 = rs.getString("code_service");
                txt_code.setText(t2);
                String t3 = rs.getString("no_chambre");
                txt_chbr.setText(t3);
                String t4 = rs.getString("lit");
                txt_lit.setText(t4);
            }
            
        }catch(SQLException e){
            System.out.println("Exeption" + e);
        }
       
    }


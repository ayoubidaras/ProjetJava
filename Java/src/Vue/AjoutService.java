    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String requete = "insert into SERVICE (code, batiment,nom, directeur) values (?, ?, ?,?)";
            
            String var = txt_dir.getText();
            String verif ="select * from DOCTEUR where numero = '"+var+"'";
            
          
            
            ps = conn.prepareStatement(verif);
            rs =  ps.executeQuery();
             
             if(rs.next())
             {
                 try{
                    ps = conn.prepareStatement(requete);
                    ps.setString(1,txt_code.getText());
                    ps.setString(2,txt_bat.getText());
                    ps.setString(3,txt_nom.getText());
                    ps.setString(4,txt_dir.getText());
                    ps.execute();

                    JOptionPane.showMessageDialog(null,"Saved");
                    dispose();
             }catch(SQLException e){
                     System.out.println("Exeption 1a" + e);
              }
             }

        }catch(SQLException e){
            System.out.println("Exeption 1b" + e);
        }finally{
            try{
                ps.close();
                rs.close();
            }catch(SQLException e){
                System.out.println("Exeption 2" + e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

